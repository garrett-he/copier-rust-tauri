def test_changelog_content(copie: Copie, base_answers: dict[str, str]) -> None:
    """Test that CHANGELOG.md contains expected content."""
    answers = {**base_answers, 'copyright_license': 'MIT', 'with_changelog': True}
    result = copie.copy(extra_answers=answers)

    assert result.exit_code == 0
    assert result.project_dir is not None

    changelog = result.project_dir / 'CHANGELOG.md'
    content = changelog.read_text()
    assert '# Changelog' in content
    assert 'Keep a Changelog' in content
    assert 'Semantic Versioning' in content
    assert '## [Unreleased]' in content
